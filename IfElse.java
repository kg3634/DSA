package DSA;

public class IfElse {

    public static void main (String[] args) {
  
        int x = 0;

        if (x == 0){
            System.out.println("Neither EVEN not odd");
        }
        else if (x % 2 == 0) {
            System.out.println("EVEN");
        }
        else {
            System.out.println("ODD");
        }

    }
    
}
